//Switch Statements

const day = 'wednesday';

switch (day) {
    case 'monday':
        console.log('Plan course structure');
        break;
    case 'tuesday':
        console.log('Prepare theory video');
        break;
    case 'wednesday':
        console.log('Do coding exersise');
        break;
    case 'thursday':
        console.log('Make some projects');
        break;
    case 'friday':
        console.log('Go for a hackthon');
        break;
    case 'saturday':
        console.log('Attend a community metup');
        break;
    case 'sunday':
        console.log('have some rest');
        break;
    default:
        console.log('Not a valid day');    
}